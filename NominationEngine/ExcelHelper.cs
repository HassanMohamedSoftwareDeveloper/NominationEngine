        private static byte[] GenerateExcel(DataSet ds)
        {
            using (MemoryStream fs = new MemoryStream())
            {
                SpreadsheetDocument document = SpreadsheetDocument.Create(fs, SpreadsheetDocumentType.Workbook, true);
                WriteExcelFile(ds, document);

                document.Save();
                document.Close();

                return fs.ToArray();
            }
        }

